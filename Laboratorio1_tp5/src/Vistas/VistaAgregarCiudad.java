    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String ciudad = txtNombreCiudad.getText().trim();
        
        if(ciudad.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese una ciudad");
            return;
        }
        
        for(String c : App.FrmMenu.CIUDADES){
            if(c.equalsIgnoreCase(ciudad)){
                JOptionPane.showMessageDialog(this, "La ciudad ya existe");
                return;
            }
        }
        
        boolean ok = App.FrmMenu.CIUDADES.add(ciudad);
        JOptionPane.showMessageDialog(this, ok ? "Ciudad Agregada" : "No se pudo agregar la ciudad");
        
        if(ok){
            txtNombreCiudad.setText("");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


