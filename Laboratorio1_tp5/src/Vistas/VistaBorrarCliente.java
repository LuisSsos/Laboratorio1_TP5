    private void listDniValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listDniValueChanged
        if(evt.getValueIsAdjusting()){
            return;
        }
        
        String dniSelect = listDni.getSelectedValue();
        if(dniSelect == null) {
            return;
        }
        
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tablaClientes.getModel();
        model.setRowCount(0);
        
        for(Long tel : App.FrmMenu.DIRECTORIO.getTelefonos()) {
            Controladores.Cliente c = App.FrmMenu.DIRECTORIO.buscarContacto(tel);
            
            if(c != null && dniSelect.equals(c.getDni())){
                model.addRow(new Object[]{c.getDni(), c.getApellido(), c.getNombre(), c.getCiudad(), c.getDomicilio(), tel});
            }
        }
    }//GEN-LAST:event_listDniValueChanged


