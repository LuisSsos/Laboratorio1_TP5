    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        int row = tablaClientes.getSelectedRow();
        
        if(row == -1){
            JOptionPane.showMessageDialog(this, "Seleccione un registro de la tabla");
            return;
        }
        
        Object telObj = tablaClientes.getValueAt(row, 5);
        Long telefono;
        if (telObj instanceof Long) {
            telefono = (Long) telObj;
        } else {
            telefono = Long.parseLong(telObj.toString());
        }
        
        boolean ok = App.FrmMenu.DIRECTORIO.borrarContacto(telefono);
        JOptionPane.showMessageDialog(this, ok ? "Eliminado" : "No se pudo eliminar el cliente");
        
        if(ok) {
            ((javax.swing.table.DefaultTableModel) tablaClientes.getModel()).removeRow(row);
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

